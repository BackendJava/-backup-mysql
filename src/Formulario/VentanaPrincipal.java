    private void ClickRespaldo(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ClickRespaldo
        if (evt.getSource() == pnlBuscarArchivoExp) {
            JFileChooser ch = new JFileChooser();
            ch.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int se = ch.showSaveDialog(null);
            if (se == JFileChooser.APPROVE_OPTION) {
                String ruta = ch.getSelectedFile().getPath();
                txtRutaExp.setText(ruta);
            }

        }
        if (evt.getSource() == pnlBuscarArchivoImp) {
            JFileChooser ch = new JFileChooser();
            FileNameExtensionFilter file = new FileNameExtensionFilter("SQL", "sql");
            ch.setFileFilter(file);

            int se = ch.showOpenDialog(null);
            if (se == JFileChooser.APPROVE_OPTION) {
                String ruta = ch.getSelectedFile().getPath();
                txtRutaImp.setText(ruta);
                System.out.println();
            }
        }
        if (evt.getSource() == btnExportar) {
            if (txtRutaExp.getText().length() != 0) {
                exportar.setRuta(txtRutaExp.getText());
                exportar.ExportarBase();
                System.out.println(txtRutaExp.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        if (evt.getSource() == btnImportar) {//boton para importar una base de datos
            ResultSet rs;
            boolean existe = false;
            if (!"".equals(txtRutaImp.getText())) {
                String NuevaBase = JOptionPane.showInputDialog("Escriba el nombre de la base de datos");
                if (NuevaBase != null) {
                    if (NuevaBase.length() != 0 && !"".equals(NuevaBase)) {
                        conexion.conexionMySQL.EjecutarConsulta("show databases");
                        rs = conexion.conexionMySQL.getResultSet();
                        try {
                            while (rs.next()) {
                                if (NuevaBase.equals(rs.getString("Database"))) {
                                    existe = true;
                                }
                            }
                            if (existe) {
                                JOptionPane.showMessageDialog(null, "La base de datos ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                            } else {
                                conexion.conexionMySQL.EjecutarInstruccion("create database "+NuevaBase);
                                importar.setRuta(txtRutaImp.getText());
                                importar.setBaseDatos(NuevaBase);
                                importar.ImportarBase();
                                fichero.setFicheroBase(NuevaBase);
                                conexion = new VariableLocal(NuevaBase);
                                txtRutaImp.setText("");
                                
                            }
                        } catch (Exception e) {
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Ingrese un nombre para la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_ClickRespaldo

